                            <h3>
                                Vomiting</h3>
                            <p>
                                <b>Vomiting is a very common problem with cats with a multitude of causes. They range
                                    from eating something poisonous or inedible (like string), to infection, urinary
                                    tract disease, or diabetes to hairballs. Symptoms are usually obvious, and include
                                    drooling and abdominal heaving. Vomiting can quickly leave your cat dehydrated,
                                    so if kitty continues vomiting or acts ill, call your vet right away. It may help
                                    to collect a sample of your cat's vomit and take it with you to the vet. </b>
                            </p>
                            <br />
                            <h3>
                                Feline Lower Urinary Tract Diseases (FLUTD)</h3>
                            <p>
                                <b>Some estimates say as many as 3% of cats seen by vets have feline lower urinary tract
                                    disease (FLUTD), which is actually a group of feline diseases with multiple causes.
                                    Female and male cats can get FLUTD, and it often occurs in cats that are overweight
                                    or unfit or who eat dry food. Stress, a multi-cat household, and sudden changes
                                    can all raise a cat's risk of FLUTD, and treatment depends on the type of FLUTD
                                    your cat has. FLUTD symptoms include:<br>
                                    · Straining to urinate<br>
                                    · Bloody urine<br>
                                    · Urinating in unusual places<br>
                                    · Crying when urinating<br>
                                    · Licking around the urinary area (often because of pain)<br>
                                    · Depression<br>
                                    · Dehydration<br>
                                    · Lack of appetite<br>
                                    · Vomiting<br>
                                </b>
                            </p>
                            <p>
                                <b>It's always an emergency if your cat can't urinate. Call your vet immediately if
                                    you suspect your cat has a urinary tract problem.<br>
                                </b>
                            </p>
                            <br />
                            <h3>
                                Fleas</h3>
                            <p>
                                <b>Fleas are a very common external feline health problem. But it's one you can easily
                                    treat. Signs your cat has fleas include:<br>
                                    · Flea dirt on its skin (they look like tiny black dots)<br>
                                    · Constant scratching<br>
                                    · Frequent licking<br>
                                    · Red or irritated skin<br>
                                    · Hair loss<br>
                                    · Skin infections or hot spots<br>
                                </b>
                            </p>
                            <p>
                                <b>Fleas can live for more than a year, and your cat risks anaemia if the problem becomes
                                    serious, so be sure to treat your cat's flea problem and prevent future infestations.
                                    Talk to your vet about which flea control would be best for your cat. Treatments
                                    include oral medication, powders, foams, and topical medication.</b>
                            </p>
                            <br />
                            <h3>
                                Tapeworms</h3>
                            <p>
                                <b>One of the most common feline health problems inside your cat, tapeworms live in
                                    kitty's small intestine and sometimes grow as long as 2 feet. that said, tapeworms
                                    are segmented and usually break apart when expelled. It's very unlikely to see a
                                    full worm. You will usually see the segments. Symptoms of a tapeworm infection can
                                    be subtle but may include vomiting and weight loss. The easiest way to tell if your
                                    cat has tapeworms is to look at its faeces, around its anus and in bedding. Usually
                                    tapeworms come out of your cat's anus while it is sleeping or relaxed. If you see
                                    small white worms or what look like grains of rice or sesame seeds, your cat likely
                                    has tapeworms. Treatment options include injection, oral, or topical medication.
                                    But because cats almost always get tapeworms as a result of swallowing a flea, be
                                    sure to handle any flea problems your cat has before tackling tapeworms. </b>
                            </p>
                            <br>
                            <h3>
                                Diarrhoea</h3>
                            <p>
                                <b>Many things can cause diarrhoea in cats, including intestinal parasites, spoiled
                                    food, allergies, infection, liver disease, cancer, and more. Symptoms of diarrhoea
                                    are loose, watery, or liquid stool. Depending on its cause, diarrhea can last for
                                    a day, a week, or months. If your cat has diarrhoea, offer kitty plenty of fresh,
                                    clean water to prevent dehydration. Then remove kitty's food for no more than 12
                                    to 24 hours. Take your cat to the vet if he or she still has diarrhoea after a day
                                    or immediately if you notice vomiting, dark, or bloody stools, fever, lethargy,
                                    or loss of appetite or if your cat is straining to defecate. </b>
                            </p>
                            <br>
                            <h3>
                                Eye Problems</h3>
                            <p>
                                <b>Eye problems in cats can be caused by a number of things, including conjunctivitis,
                                    cataracts, glaucoma, trauma, viruses, inflammation, and retinal disease. A few symptoms
                                    that may mean your cat has eye problems include watery eyes, tear-stained fur, cloudiness,
                                    red or white eyelid linings, gunk in the corners of the eye, squinting, pawing at
                                    the eye, or a visible third eyelid. Unless you know what's causing your cat's eye
                                    problems, there isn't much you can do other than call your vet. Eye problems should
                                    be considered an emergency so make appointment immediately. </b>
                            </p>
