                            <h3>
                                Dogs and Ear Infections:</h3>
                            <p>
                                <strong>Ear infections are a common canine health problem, and they can be caused by
                                    allergies, yeast, ear mites, bacteria, hair growth deep in the ear canal, and more.
                                    Symptoms your dog may have with an ear infection include:</strong></p>
                            <p>
                                <strong>· Head shaking or head tilting<br />
                                    · Ear odour<br />
                                    · Vigorous scratching<br />
                                    · Lack of balance<br />
                                    · Unusual back-and-forth eye movements<br />
                                    · Redness of the ear canal<br />
                                    · Swelling of the outer portion of the ear<br />
                                    · Brown, yellow, or bloody discharge</strong>
                            </p>
                            <p>
                                <strong>Always take your dog to the veterinarian if you think he has an ear infection.
                                    In most cases, cleaning and medicating the ear canal will quickly clear up an infection.
                                    However, surgery can be needed for chronic infections or if forceful head shaking
                                    results in the rupture of a vessel within the outer part of the ear. </strong>
                            </p>
                            <br />
                            <h3>
                                Dogs and Worms</h3>
                            <p>
                                <strong>Tapeworms, roundworms, hookworms, and whipworms are common internal parasites
                                    in dogs. And although any worm infestation can make your pooch uncomfortable, some,
                                    like hookworms, can be fatal in puppies. Signs your dog may have worms include:
                                    · Diarrhoea (may be bloody)<br />
                                    · Weight loss<br />
                                    · A change in appetite<br />
                                    · A rough, dry coat<br />
                                    · Scooting on his bottom<br />
                                    · Vomiting (with roundworms in particular)<br />
                                    · An overall poor appearance </strong>
                            </p>
                            <p>
                                <strong>The best way to diagnose a worm problem is with a visit to the vet. Treatment
                                    depends on which type of worm your dog has, but generally includes an oral medication
                                    and may require follow-up. Don't try treating worms yourself: A medication that
                                    kills roundworms, for example, doesn't kill tapeworms.</strong></p>
                            <br />
                            <h3>
                                When Fleas Find Your Dog</h3>
                            <p>
                                <strong>It takes just three weeks for one flea to turn into an infestation of 1,000
                                    biting bugs. A very common canine health problem, fleas are easy for your dog to
                                    pick up, but they're also easy to treat. Signs your dog may have fleas include:<br />
                                    · Excessive scratching, licking, or biting at the skin<br />
                                    · Hair loss<br />
                                    · Hot spots<br />
                                    · Allergic dermatitis (allergic response caused by contact)<br />
                                    · Tapeworms (which are carried by fleas)<br />
                                    · Flea dirt (looks like small black dots) against your dog's skin</strong></p>
                            <p>
                                <strong>Untreated, fleas not only make your dog intensely uncomfortable, they can also
                                    cause allergic reactions, infections, and even lead to anaemia from blood loss.
                                    Talk to your vet about the right flea medicine for your dog, which may include collars,
                                    oral medicine, shampoos, sprays, or topical liquids. </strong>
                            </p>
                            <br />
                            <h3>
                                Common Dog Health Problem: Vomiting</h3>
                            <p>
                                <strong>Vomiting is a common dog health problem, with dozens of possible causes, from
                                    infection or intestinal parasites to pancreatitis, kidney failure, heatstroke, or
                                    poisoning.</strong>
                            </p>
                            <p>
                                <strong>Symptoms are basic: abdominal heaving and drooling caused by nausea. If your
                                    dog also has diarrhoea, blood in the vomit, seems lethargic, continues vomiting,
                                    or can't hold down liquids, contact your vet right away to prevent life-threatening
                                    dehydration.</strong>
                            </p>
                            <p>
                                <strong>Treatment depends on what's causing a dog's distress, and may include fluid
                                    therapy, drugs to control vomiting, and homemade foods like well-cooked skinless
                                    chicken, boiled potatoes, and rice.</strong>
                            </p>
                            <br />
                            <h3>
                                Doggy Diarrhoea</h3>
                            <p>
                                <strong>Diarrhoea in dogs, as with vomiting, can have lots of causes, including stress,
                                    infections like parvo virus, intestinal parasites, and food problems. Diarrhoea
                                    symptoms are pretty obvious - look for loose, watery, or liquid stool. Diarrhoea
                                    can easily lead to dehydration, be sure your dog has plenty of clean water available,
                                    then take your pooch to the vet if the diarrhoea persists for more than a day, or
                                    immediately if there's also fever, lethargy, vomiting, dark or bloody stools, or
                                    loss of appetite.</strong></p>
