                        <h1 class="hdtitle boxhdcolor4">
                            FISH</h1>
                        <div class="text-desc text-center">
                            <p>
                                Fish diseases and common illnesses. Please consult a local veterinary doctor to
                                properly diagnose ailments and fine-tune treatments. Diseases in fish are mostly
                                caused by microorganisms, as a precautionary measure and to eliminate them, the
                                water must be kept fresh at all times.
                            </p>
                            <h3>
                                FISH ICK</h3>
                            <p>
                                < <b>Physical/Behavioural Signs/Symptoms:</b>
                                <br />
                                Spots that resemble grains of salt or white sand on the skin.
                                <br />
                                May be slightly-raised.
                                <br />
                                Scratching against objects due to irritated skin
                                <br />
                                Clamped fins.
                                <br />
                                Gasping at the water’s surface.<br />
                                <br />
                                <b>Cause:</b><br />
                                Usually attacks fish that are stressed, which can be caused by factors including
                                rapid temperature and pH fluctuations.
                            </p>
                            <br />
                            <h3>
                                RAGGED TAIL FIN</h3>
                            <p>
                                <b>Physical/Behavioural Signs or Symptoms:</b><br />
                                A progressive deterioration of the tail and/or fins.
                                <br />
                                Fins become frayed or their color may fade.
                            </p>
                            <p>
                                <b>Cause:</b><br />
                                A bacterial infection may cause this tail and fin rot in susceptible fish—those
                                who are bullied or injured by fin-nipping tank mates—especially in aquariums with
                                poor conditions.</p>
                            <br />
                            <h3>
                                TAIL, FIN, AND MOUTH ROT</h3>
                            <br />
                            <p>
                                <b>Physical/Behavioural Signs or Symptoms:</b><br />
                                A progressive deterioration of the tail and/or fins.<br />
                                Fins become frayed or their color may fade.
                            </p>
                            <p>
                                <b>Cause:</b><br />
                                A bacterial infection may cause tail, fin and mouth rot in susceptible fish—those
                                who are bullied or injured by fin-nipping tank mates—especially in aquariums with
                                poor conditions.
                            </p>
                            <p>
                                <b>Note: Always remove activated carbon before administering any treatments.</b></p>
                            <h3>
                                FUNGUS</h3>
                            <p>
                                <b>Physical/Behavioural Signs or Symptoms:</b><br />
                                Initially, you’ll notice a gray or whitish growth in and on the skin and/or fins.<br />
                                Untreated fungus resembles a cottony growth.
                                <br />
                                Eventually, as fungus continues to eat away at the fish’s body, the fish will die.<br />
                            </p>
                            <p>
                                <b>Cause:</b><br />
                                Fish who develop fungus are already in a vulnerable state, the result of other serious
                                health problems or attacks, such as parasites, a physical injury or a bacterial
                                infection.</p>
                            <br />
                            <h3>
                                CLAMPED FIN</h3>
                            <p>
                                <b>Physical/Behavioral Signs or Symptoms:</b><br />
                                Fins are folded against the body and not fanned out as they should be.<br />
                                Listless behavior.
                            </p>
                            <p>
                                <b>Cause:</b><br />
                                Not indicative of one specific disease. Can be a reflection of various problems,
                                including bad water quality and/or parasites. Important to first determine the specific
                                problem in order to treat fish properly.</p>
                            <p>
                                <b>Note: Always remove activated carbon before administering any treatments.</b></p>
                            <h3>
                                DROPSY</h3>
                            <p>
                                <b>Physical/Behavioral Signs or Symptoms:</b><br />
                                Bloating.<br />
                                Protruding scales.
                            </p>
                            <p>
                                <b>Cause:</b><br />
                                A bacterial infection of the kidneys, which causes fluid accumulation or renal failure.
                                It appears to create problems only in weakened fish. May stem from untidy aquarium
                                conditions.
                            </p>
                            <br />
                            <h3>
                                ANCHOR WORMS</h3>
                            <p>
                                <b>Physical/Behavioral Signs or Symptoms:</b><br />
                                Scratching against objects by the affected fish.
                                <br />
                                A protuberance of whitish-green threads from the fish's skin.<br />
                                Points of attachment are marked by inflammation.
                            </p>
                            <p>
                                <b>Cause:</b><br />
                                Introduced into aquariums by infected fish, young anchor worms are small crustaceans
                                that burrow into the fish’s skin and enter the muscles. Here they begin to develop
                                and release eggs before they die—leaving behind damage, which can become infected.<br />
                                Common methods of treatment include physically removing the parasite and cleaning
                                the wound with an antiseptic like iodine, also common is bathing freshwater fish
                                in a seawater bath (35ppt) for about 5 minutes for multiple days until the parasite
                                falls off.</p>
                            <br />
                            <h3>
                                BODY FLUKES & GILL FLUKES (flatworms approximately 1 mm long)</h3>
                            <p>
                                <b>Physical/Behavioral Signs or Symptoms of <b>BODY FLUKES</b>:</b><br />
                                Scratching against objects by the affected fish.
                                <br />
                                Layer of mucus covering gills or body.
                                <br />
                                Gills moving rapidly.
                                <br />
                                Chewed on or eaten-away gills or fins.
                                <br />
                                Reddened skin.
                            </p>
                            <p>
                                <b>NOTE: Pale fish with drooping fins, rapid respiration and/or hollow bellies indicate
                                    more extensive infestation. </b>
                            </p>
                            <p>
                                <b>Physical/Behavioral Signs or Symptoms of <b>GILL FLUKES</b>:</b><br />
                                Infected gills and skin
                                <br />
                                Similar to ick, but telltale sign is movement and possibly eyespots, something that
                                is not found in ick. Use magnification lens to observe.
                                <br />
                                Once gills are destroyed, fish will die.<br />
                                <br />
                            </p>
                            <p>
                                <b>Cause:</b><br />
                                Undesirable environmental conditions—including poor water quality, overcrowding,
                                and/or stress by incompatible species—creates conditions that can lead to destructive
                                outbreaks. Flukes are often present in aquariums but remain harmless under ideal
                                conditions. Avoiding stressful conditions is a key to prevention, but once an outbreak
                                occurs, prompt treatment is critical.</p>
                            <br />
                            <h3>
                                LICE</h3>
                            <p>
                                <b>Physical/Behavioral Signs or Symptoms:</b><br />
                                Red spots on fish indicating an inflammation.<br />
                                Fish are aggravated and restless.
                                <br />
                                Usually fish will rub skin against aquarium glass or other objects in an effort
                                to remove lice.<br />
                                Other telltale signs: Lice have eight legs—and resemble tiny pale crabs. They appear
                                as flat, dark oval dots crawling on fish. Lice use suckers to attach to fish. Then
                                they pierce the skin and feed on the host.
                            </p>
                            <p>
                                <b>Cause:</b><br />
                                Usually from fish that were living in an outdoor pond at one time and bringing them
                                into an indoor aquarium. Also, fish lice can be introduced from wild fish that are
                                added to an aquarium. Lice travel from one host fish to another, spreading bacteria
                                and viruses, so once they’re in your aquarium, you must get rid of them.<br />
                                Common methods of treatment include physically removing the parasite and cleaning
                                the wound with an antiseptic like iodine, as well as bathing freshwater fish in
                                a seawater bath (35ppt) for about 5 minutes for multiple days until the parasite
                                falls off, or using a formalin bath.</p>
                            <br />
                            <h3>
                                GILL MITES</h3>
                            <p>
                                <b>Physical/Behavioral Signs or Symptoms:</b>
                                <br />
                                Gasping at the water’s surface.
                                <br />
                                Gill covers that are partially open.
                            </p>
                            <p>
                                <b>Cause:</b><br />
                                Fish that are already infested by gill mites are brought into aquariums. The tiny
                                mites stay on the fish’s gills, and attack the fish by feeding on blood and living
                                flesh.</p>
