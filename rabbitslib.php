                        <h1 class="hdtitle boxhdcolor4">
                            RABBITS</h1>
                        <div class="text-desc text-center">
                            <p>
                                There are some common diseases and problems seen in rabbits that can be prevented
                                by ensuring you have an understanding of what a healthy rabbit requires and the
                                subtle signs that can tell you your rabbit is unwell. Rabbits are wonderful domesticated
                                pets, but it should be remembered that they are very closely related to wild rabbits,
                                and as such will hide signs of illness until they are very unwell, as this would
                                make them “easy prey” in nature. We encourage close observations of your pet rabbit,
                                a correct diet, up to date vaccinations and regular health checks to ensure you
                                pick up the early signs of problems, and prevent diseases developing.
                                <br />
                                The more common problems/ diseases that we see in domesticated rabbits are:
                            </p>
                            <br />
                            <h3>
                                OVERGROWN TEETH</h3>
                            <p>
                                <b>Cause and Clinical Signs</b><br />
                                A rabbit’s teeth continually grow throughout its life and if a rabbit is not constantly
                                grinding their teeth down by eating fibre we start to see their molar teeth forming
                                sharp spikes that damage their cheeks and tongue. This causes pain that makes them
                                reluctant or unable to eat. The incisors at the front of the mouth can, in severe
                                cases grow around in a curl meaning rabbits cannot close their mouth or eat at all.
                                Once a rabbit stops eating their gut stops working and they can die.
                            </p>
                            <p>
                                <b>Prevention</b><br />
                                80-90% of your rabbit’s diet needs to be fibre in the form of oaten or grass hay.
                                The rest of the diet should be leafy greens, with pellets and other treats being
                                minimal to non-existent.
                            </p>
                            <p>
                                <b>Treatment</b><br />
                                A general anaesthetic and burring the teeth flat is the only treatment that can
                                correct overgrown teeth.</p>
                            <br />
                            <h3>
                                SNUFFLES (PASTEURELLOSIS)</h3>
                            <p>
                                <b>Cause and Clinical Signs</b><br />
                                Close contact with an infected rabbit can easily transfer the Pasteurella multocida
                                bacterium to your rabbit. The bacteria can affect the eyes (discharge, redness,
                                squinting) and/or nose (sneezing, discharge), thus giving the disease its name “snuffles.”
                                Pasteurellacan infect other areas of the body as well, including ears (resulting
                                in a head tilt), abscesses (seen as lumps on the body) and uterine infections.
                            </p>
                            <p>
                                <b>Prevention</b><br />
                                Some strains of the bacteria remain latent in the rabbit’s nasal tract until the
                                immune system is placed under stress, such as when a new diet or pet is introduced
                                or if overcrowding occurs. Reducing stress for rabbits that are infected and the
                                quarantine of new rabbits are good ways of preventing introduction of the disease
                                or recurrence of signs.</p>
                            <p>
                                <b>Treatment</b><br />
                                Treatment involves a long and sometimes repeated course of antibiotics. Occasionally
                                surgery is required if an abscess forms.</p>
                            <h3>
                                HAIRBALLS (TRICHOBEZOARS)</h3>
                            <p>
                                <b>Cause and Clinical Signs</b><br />
                                Hair can normally be found in a rabbit’s stomach as they self-groom. However, as
                                rabbits cannot vomit, hair must be able to pass through the gut. If it can’t then
                                it will form an obstruction and serious complications. Hairballs are so common that
                                they should always be considered as a problem in any rabbit that is lethargic and
                                not eating.</p>
                            <p>
                                <b>Prevention</b><br />
                                Hairballs are more likely to become a problem if there is an issue with the gastrointestinal
                                tract (gut stasis) or they are not receiving enough fibre in their diet, so a high
                                fibre diet is a great preventative measure.</p>
                            <p>
                                <b>Treatment</b><br />
                                Sometimes surgery is the only treatment if the hairball causes a blockage in the
                                gut. Medication to get the gut working again can also help.
                            </p>
                            <br />
                            <h3>
                                UTERINE TUMOURS</h3>
                            <p>
                                <b>Cause and Clinical Signs</b><br />
                                Entire female rabbits can develop a cancer called uterine adenocarcinoma and it
                                should be suspected anytime an un-desexed female rabbit becomes sick. Some of the
                                more common clinical signs include blood stained vaginal discharge, aggressive behaviour,
                                mammary gland cysts and lethargy.</p>
                            <p>
                                <b>Prevention</b><br />
                                Desexing early at 4-6 months of age.</p>
                            <p>
                                <b>Treatment</b><br />
                                Desexing, hopefully prior to the cancer spreading through the body.</p>
                            <br />
                            <h3>
                                MYXOMATOSIS</h3>
                            <p>
                                <b>Cause and Clinical Signs</b><br />
                                Myxomatosis is a virus that is transmitted by mosquitoes, fleas or by close contact
                                between an infected rabbit and a susceptible rabbit. The disease is recognised by
                                swelling and discharge from the eyes, nose and anogenital region.</p>
                            <p>
                                <b>Prevention</b><br />
                                Unfortunately there is no vaccination available in Australia. Invest in a mosquito
                                proof rabbit hutch or bring your rabbit inside at dawn and dusk when mosquito levels
                                are higher. Flea control can help and when introducing new rabbits keep them isolated
                                for at least 2 weeks.</p>
                            <p>
                                <b>Treatment</b><br />
                                The disease is invariably fatal.<br />
                            </p>
                            <h3>
                                CALICIVIRUS</h3>
                            <p>
                                <b>Cause and Clinical Signs</b><br />
                                Calicivirus is spread by mosquito or through direct contact with an infected rabbit.
                                This is a disease that damages the rabbit’s internal organs such as the liver and
                                gut, and can also cause haemorrhage or bleeding. The symptoms include going off
                                food, being depressed and quiet, and you may notice some bleeding from the nose.
                                The disease can progress very quickly and is often fatal.</p>
                            <p>
                                <b>Prevention</b><br />
                                Luckily there is a vaccination against calicivirus. It should be given annually
                                to ensure your rabbit’s immunity against it is maintained. Mosquito proof housing
                                as well as avoiding infected rabbits is also advised.</p>
                            <p>
                                <b>Treatment</b><br />
                                There is no treatment available.
                            </p>
                            <p>
                                <b>NOTE: Often there are subtle changes in your rabbit’s behaviour that may be a clue
                                    to a disease process occurring. If you have noticed any changes or are concerned
                                    about something your bunny has been doing then it is advised to get them checked
                                    by your vet. The earlier problems are detected the better your rabbit’s chances
                                    are of living a healthy life.</b></p>
